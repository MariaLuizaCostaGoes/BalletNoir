function pegarDescricaoVariacao(idVariacao) {
    fetch(`/usuarios/${idVariacao}`, {cache: 'no-store'}).then(function(response) {
        if (response.ok) {
            response.json().then(function(resposta) {
                return resposta[0]
            })
        }
    })
}

